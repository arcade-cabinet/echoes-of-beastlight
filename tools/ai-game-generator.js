#!/usr/bin/env node

const fs = require('fs').promises;
const path = require('path');
const yaml = require('js-yaml');
const chalk = require('chalk');
const { OpenAI } = require('openai');
const pLimit = require('p-limit');
const { LRUCache } = require('lru-cache');

/**
 * AI Game Generator - Optimized for direct AI execution
 * 
 * This generator is designed to be run directly by AI assistants,
 * with full control over the generation process and optimal patterns
 * for LLM understanding and manipulation.
 */

class AIGameGenerator {
  constructor() {
    this.openai = new OpenAI({
      apiKey: process.env.OPENAI_API_KEY,
    });
    
    this.cache = new LRUCache({
      max: 1000,
      ttl: 1000 * 60 * 60 * 24, // 24 hours
    });
    
    this.limit = pLimit(5); // Higher concurrency for AI execution
    
    // Generation state tracking
    this.state = {
      phase: 'initializing',
      progress: {},
      generated: new Set(),
      errors: [],
    };
    
    // Load config on construction
    this.configPath = 'game-config.yaml';
  }
  
  async initialize() {
    console.log(chalk.cyan('🤖 AI Game Generator - Initializing...'));
    
    // Load game configuration
    const configContent = await fs.readFile(this.configPath, 'utf-8');
    this.config = yaml.load(configContent);
    
    // Create directory structure
    await this.setupDirectories();
    
    // Load generation templates
    await this.loadTemplates();
    
    console.log(chalk.green('✓ Initialization complete'));
    return this;
  }
  
  // Helper to write files and track them
  async writeFile(path, content) {
    await fs.writeFile(path, content);
    this.state.generated.add(path);
  }
  
  async setupDirectories() {
    const dirs = [
      'src',
      'src/components',
      'src/systems',
      'src/tilemaps',
      'src/levels',
      'src/ai',
      'assets/sprites',
      'assets/audio',
      'assets/data',
      'assets/levels',
      'assets/quests',
      '.cache/ai-gen',
    ];
    
    for (const dir of dirs) {
      await fs.mkdir(dir, { recursive: true });
    }
  }
  
  async loadTemplates() {
    // Instead of external templates, use embedded AI-optimized patterns
    this.templates = {
      rust: {
        component: (name, fields) => `
#[derive(Component, Debug, Clone)]
pub struct ${name} {
${fields.map(f => `    pub ${f.name}: ${f.type},`).join('\n')}
}

impl ${name} {
    pub fn new(${fields.map(f => `${f.name}: ${f.type}`).join(', ')}) -> Self {
        Self { ${fields.map(f => f.name).join(', ')} }
    }
}`,
        system: (name, components, logic) => `
pub fn ${name}_system(
    ${components.map(c => `${c.query}: Query<${c.types}>`).join(',\n    ')}
) {
    ${logic}
}`,
        tilemap: (zone, layers, tiles) => `
use bevy::prelude::*;
use bevy_ecs_tilemap::prelude::*;

pub fn setup_${zone}_tilemap(
    mut commands: Commands,
    asset_server: Res<AssetServer>,
) {
    let texture_handle = asset_server.load("sprites/${zone}_tiles.png");
    
    ${layers.map((layer, i) => `
    // Layer ${i}: ${layer.name}
    let map_size = TilemapSize { x: 32, y: 32 };
    let tilemap_entity = commands.spawn_empty().id();
    let mut tile_storage = TileStorage::empty(map_size);
    
    // Generate tiles for ${layer.name}
    ${tiles}
    
    commands.entity(tilemap_entity).insert(TilemapBundle {
        grid_size: TilemapGridSize { x: 16.0, y: 16.0 },
        size: map_size,
        storage: tile_storage,
        texture: TilemapTexture::Single(texture_handle.clone()),
        tile_size: TilemapTileSize { x: 16.0, y: 16.0 },
        transform: Transform::from_xyz(0.0, 0.0, ${layer.z_index}.0),
        ..Default::default()
    });`).join('\n')}
}`,
      },
      
      yaml: {
        monster: (data) => yaml.dump(data, { noRefs: true }),
        level: (data) => yaml.dump(data, { noRefs: true }),
        quest: (data) => yaml.dump(data, { noRefs: true }),
      },
      
      yoleck: {
        level: (name, entities) => `
# Bevy-Yoleck Level File
# Generated by AI Game Generator

name: ${name}
format_version: 2

entities:
${entities.map(e => `  - type: ${e.type}
    name: ${e.name}
    components:
      Transform:
        translation: [${e.x}, ${e.y}, ${e.z || 0}]
      ${Object.entries(e.components || {}).map(([k, v]) => 
        `${k}: ${typeof v === 'object' ? '\n' + yaml.dump(v, {indent: 8}) : v}`
      ).join('\n      ')}`).join('\n')}`,
      },
    };
  }
  
  // Core generation method optimized for AI
  async generate(prompt, options = {}) {
    const {
      model = 'gpt-4',
      temperature = 0.7,
      maxTokens = 4000,
      format = 'text',
      cache = true,
    } = options;
    
    // Check cache
    const cacheKey = JSON.stringify({ prompt, model, temperature });
    if (cache && this.cache.has(cacheKey)) {
      console.log(chalk.blue('↺ Using cached result'));
      return this.cache.get(cacheKey);
    }
    
    try {
      const response = await this.openai.chat.completions.create({
        model,
        messages: [{ role: 'user', content: prompt }],
        temperature,
        max_tokens: maxTokens,
      });
      
      let result = response.choices[0].message.content;
      
      // Parse based on format
      if (format === 'json') {
        // Extract from markdown code blocks if present
        const jsonMatch = result.match(/```json\n([\s\S]*?)\n```/);
        if (jsonMatch) {
          result = JSON.parse(jsonMatch[1]);
        } else {
          result = JSON.parse(result);
        }
      } else if (format === 'yaml') {
        // Extract from markdown code blocks if present
        const yamlMatch = result.match(/```ya?ml\n([\s\S]*?)\n```/);
        if (yamlMatch) {
          result = yaml.load(yamlMatch[1]);
        } else {
          result = yaml.load(result);
        }
      }
      
      // Cache result
      if (cache) {
        this.cache.set(cacheKey, result);
      }
      
      return result;
    } catch (error) {
      this.state.errors.push({ prompt, error: error.message });
      throw error;
    }
  }
  
  // High-level generation methods
  async generateGame() {
    console.log(chalk.bold.cyan('\n🎮 Starting AI Game Generation\n'));
    
    this.state.phase = 'core';
    await this.generateCore();
    
    this.state.phase = 'components';
    await this.generateComponents();
    
    this.state.phase = 'systems';
    await this.generateSystems();
    
    this.state.phase = 'levels';
    await this.generateLevels();
    
    this.state.phase = 'assets';
    await this.generateAssets();
    
    this.state.phase = 'complete';
    await this.generateSummary();
    
    console.log(chalk.bold.green('\n✅ Game generation complete!\n'));
  }
  
  async generateCore() {
    console.log(chalk.yellow('📦 Generating core files...'));
    
    // Generate Cargo.toml
    const cargoPrompt = `Generate a Cargo.toml for a Bevy game called "${this.config.game.title}".
Include these exact dependencies: ${JSON.stringify(this.config.build.dependencies, null, 2)}
Add workspace configuration, WASM support, and optimized release profiles.
Output only the Cargo.toml content, no explanations.`;
    
    const cargoContent = await this.generate(cargoPrompt, { temperature: 0.2 });
    await fs.writeFile('Cargo.toml', cargoContent);
    this.state.generated.add('Cargo.toml');
    console.log(chalk.green('  ✓ Cargo.toml'));
    
    // Generate main.rs with Bevy app setup
    const mainPrompt = `Generate a main.rs for a Bevy game with:
- Title: "${this.config.game.title}"
- Window size based on ${this.config.graphics.tile_size}px tiles
- States: Menu, Playing, Paused
- Plugins: DefaultPlugins, TilemapPlugin, YoleckPlugin
- Basic camera setup for ${this.config.graphics.perspective} view
Output only Rust code, no explanations.`;
    
    const mainContent = await this.generate(mainPrompt, { temperature: 0.3 });
    await fs.writeFile('src/main.rs', mainContent);
    this.state.generated.add('src/main.rs');
    console.log(chalk.green('  ✓ main.rs'));
  }
  
  async generateComponents() {
    console.log(chalk.yellow('🧩 Generating ECS components...'));
    
    const components = [
      { name: 'Player', fields: [
        { name: 'health', type: 'i32' },
        { name: 'mana', type: 'i32' },
        { name: 'level', type: 'u32' },
      ]},
      { name: 'Monster', fields: [
        { name: 'species', type: 'String' },
        { name: 'health', type: 'i32' },
        { name: 'damage', type: 'i32' },
        { name: 'tameable', type: 'bool' },
      ]},
      { name: 'Position', fields: [
        { name: 'x', type: 'f32' },
        { name: 'y', type: 'f32' },
      ]},
    ];
    
    let componentsFile = `use bevy::prelude::*;\n\n`;
    
    for (const comp of components) {
      componentsFile += this.templates.rust.component(comp.name, comp.fields) + '\n';
    }
    
    await fs.writeFile('src/components.rs', componentsFile);
    console.log(chalk.green('  ✓ components.rs'));
  }
  
  async generateSystems() {
    console.log(chalk.yellow('⚙️  Generating game systems...'));
    
    // Movement system
    const movementSystem = this.templates.rust.system(
      'movement',
      [{ query: 'mut query', types: '&mut Position, &Velocity' }],
      `for (mut pos, vel) in query.iter_mut() {
        pos.x += vel.x;
        pos.y += vel.y;
    }`
    );
    
    await fs.writeFile('src/systems/movement.rs', 
      `use bevy::prelude::*;\nuse crate::components::*;\n\n${movementSystem}`
    );
    console.log(chalk.green('  ✓ movement system'));
    
    // Generate more systems in parallel
    const systemTasks = [
      this.generateCombatSystem(),
      this.generateTamingSystem(),
      this.generateInventorySystem(),
    ];
    
    await Promise.all(systemTasks);
  }
  
  async generateLevels() {
    console.log(chalk.yellow('🗺️  Generating levels with mapgen algorithms...'));
    
    const zones = this.config.environments.outdoor_zones;
    const tasks = [];
    
    for (const zone of zones.slice(0, 3)) { // First 3 zones
      tasks.push(this.limit(async () => {
        const algorithm = this.config.environments.map_generation.mapgen_algorithms[
          zone.type === 'dungeon' ? 'dungeon' : 'overworld'
        ];
        
        // Generate level layout
        const levelPrompt = `Generate a level layout for "${zone.name}" using ${algorithm} algorithm.
Size: 50x50 tiles
Output a Bevy-Yoleck format with:
- Player spawn point
- ${zone.type === 'starter' ? '5-10' : '10-20'} monster spawn points
- 2-3 treasure locations
- Exit point
Format as YAML with entity positions.`;
        
        const levelData = await this.generate(levelPrompt, { 
          format: 'yaml',
          temperature: 0.8,
        });
        
        const yoleckContent = this.templates.yoleck.level(zone.name, levelData.entities || []);
        const filename = `assets/levels/${zone.name.toLowerCase().replace(/\s+/g, '_')}.yol`;
        
        await fs.writeFile(filename, yoleckContent);
        console.log(chalk.green(`  ✓ ${zone.name} level`));
      }));
    }
    
    await Promise.all(tasks);
  }
  
  async generateAssets() {
    console.log(chalk.yellow('🎨 Generating asset specifications...'));
    
    // Generate sprite descriptions
    const spritePrompt = `Create pixel art sprite descriptions for a JRPG:
1. Hero "${this.config.hero.name}" - ${this.config.hero.description}
2. First 5 monsters based on combinations of: ${this.config.generation_rules.nouns.slice(0, 3).join(', ')}
Include color palettes, animation frames, and size (32x32px).
Output as structured JSON.`;
    
    const sprites = await this.generate(spritePrompt, { 
      format: 'json',
      temperature: 0.8,
    });
    
    await fs.writeFile('assets/sprites/sprite_specs.json', JSON.stringify(sprites, null, 2));
    console.log(chalk.green('  ✓ sprite specifications'));
    
    // Generate audio specifications
    const audioPrompt = `Design retro game audio for "${this.config.game.title}":
- Title theme (mysterious, ${this.config.audio.music_style})
- Battle theme (intense, fast-paced)
- Victory fanfare (triumphant, short)
- 5 sound effects: menu select, attack, item pickup, level up, tame success
Include BPM, instruments, and duration.
Output as structured JSON.`;
    
    const audio = await this.generate(audioPrompt, {
      format: 'json',
      temperature: 0.7,
    });
    
    await fs.writeFile('assets/audio/audio_specs.json', JSON.stringify(audio, null, 2));
    console.log(chalk.green('  ✓ audio specifications'));
    
    // Generate actual assets
    console.log(chalk.yellow('🖼️  Generating actual image assets...'));
    await this.generateActualSprites();
    
    console.log(chalk.yellow('🎵 Generating actual audio assets...'));
    await this.generateActualAudio();
  }
  
  async generateActualSprites() {
    // Generate images using DALL-E 3
    const heroSpritePrompt = `Pixel art sprite of a young hero named Cael for a JRPG game. 
32x32 pixels, green cloak, brown hair, crystal arm bracer (light blue), facing right in standing pose. 
Clean pixel art style with clear outlines, suitable for a retro RPG.`;
    
    try {
      const response = await this.openai.images.generate({
        model: "dall-e-3",
        prompt: heroSpritePrompt,
        n: 1,
        size: "1024x1024", // DALL-E 3 only supports 1024x1024, 1792x1024, or 1024x1792
        quality: "standard",
        response_format: "b64_json"
      });
      
      if (response.data[0].b64_json) {
        const buffer = Buffer.from(response.data[0].b64_json, 'base64');
        await fs.writeFile('assets/sprites/hero_dalle.png', buffer);
        this.state.generated.add('assets/sprites/hero_dalle.png');
        console.log(chalk.green('  ✓ hero_dalle.png (DALL-E 3)'));
      }
    } catch (error) {
      console.log(chalk.yellow(`  ⚠ DALL-E 3 generation failed: ${error.message}`));
    }
    
    // Generate a tileset
    const tilesetPrompt = `Pixel art tileset for a JRPG game showing a 2x2 grid of 16x16 pixel tiles:
Top-left: grass tile with small details
Top-right: stone/rock tile with cracks
Bottom-left: water tile with subtle animation suggestion
Bottom-right: dirt/earth tile with texture
Clean pixel art style, vibrant colors, suitable for retro RPG.`;
    
    try {
      const tilesetResponse = await this.openai.images.generate({
        model: "dall-e-3",
        prompt: tilesetPrompt,
        n: 1,
        size: "1024x1024",
        quality: "standard",
        response_format: "b64_json"
      });
      
      if (tilesetResponse.data[0].b64_json) {
        const buffer = Buffer.from(tilesetResponse.data[0].b64_json, 'base64');
        await fs.writeFile('assets/sprites/tileset_dalle.png', buffer);
        this.state.generated.add('assets/sprites/tileset_dalle.png');
        console.log(chalk.green('  ✓ tileset_dalle.png (DALL-E 3)'));
      }
    } catch (error) {
      console.log(chalk.yellow(`  ⚠ Tileset generation failed: ${error.message}`));
    }
    
    // Generate monster sprite
    const monsterPrompt = `Pixel art sprite of a Crystal Beast monster for a JRPG game.
32x32 pixels, creature made of angular blue crystals with glowing core, four legs, facing left.
Clean pixel art style with magical glow effect, suitable for retro RPG enemy.`;
    
    try {
      const monsterResponse = await this.openai.images.generate({
        model: "dall-e-3",
        prompt: monsterPrompt,
        n: 1,
        size: "1024x1024",
        quality: "standard",
        response_format: "b64_json"
      });
      
      if (monsterResponse.data[0].b64_json) {
        const buffer = Buffer.from(monsterResponse.data[0].b64_json, 'base64');
        await fs.writeFile('assets/sprites/crystal_beast_dalle.png', buffer);
        this.state.generated.add('assets/sprites/crystal_beast_dalle.png');
        console.log(chalk.green('  ✓ crystal_beast_dalle.png (DALL-E 3)'));
      }
    } catch (error) {
      console.log(chalk.yellow(`  ⚠ Monster generation failed: ${error.message}`));
    }
  }
  
  async generateActualAudio() {
    // OpenAI currently offers TTS (text-to-speech) but not music generation
    // For actual game audio, we'd need to use specialized services like:
    // - Mubert API for music
    // - ElevenLabs for voice
    // - Or generate with code (Web Audio API)
    
    console.log(chalk.yellow('  ℹ️  Audio generation requires specialized APIs'));
    
    // Generate procedural audio descriptions that could be used with Web Audio API
    const audioSpecs = {
      menu_theme: {
        type: 'melody',
        tempo: 120,
        key: 'C_major',
        pattern: [
          { note: 'C4', duration: 0.25, time: 0 },
          { note: 'E4', duration: 0.25, time: 0.25 },
          { note: 'G4', duration: 0.25, time: 0.5 },
          { note: 'C5', duration: 0.25, time: 0.75 },
        ],
        synth: 'square',
        loop: true
      },
      coin_pickup: {
        type: 'sfx',
        envelope: { attack: 0.01, decay: 0.1, sustain: 0.3, release: 0.2 },
        frequencies: [523.25, 659.25, 783.99], // C5, E5, G5
        duration: 0.3
      },
      menu_select: {
        type: 'sfx',
        waveform: 'square',
        frequency: 440,
        duration: 0.1,
        volume: 0.5
      }
    };
    
    await fs.writeFile('assets/audio/procedural_audio_specs.json', JSON.stringify(audioSpecs, null, 2));
    this.state.generated.add('assets/audio/procedural_audio_specs.json');
    console.log(chalk.green('  ✓ procedural_audio_specs.json'));
    
    // If we had access to OpenAI's Whisper or future audio generation:
    // const audioResponse = await this.openai.audio.create({...})
    
    // For now, generate a simple Web Audio API script
    const webAudioScript = `// Web Audio API script for generating game sounds
class GameAudio {
  constructor() {
    this.context = new (window.AudioContext || window.webkitAudioContext)();
  }
  
  playMenuSelect() {
    const oscillator = this.context.createOscillator();
    const gainNode = this.context.createGain();
    
    oscillator.connect(gainNode);
    gainNode.connect(this.context.destination);
    
    oscillator.type = 'square';
    oscillator.frequency.setValueAtTime(440, this.context.currentTime);
    gainNode.gain.setValueAtTime(0.3, this.context.currentTime);
    gainNode.gain.exponentialRampToValueAtTime(0.01, this.context.currentTime + 0.1);
    
    oscillator.start(this.context.currentTime);
    oscillator.stop(this.context.currentTime + 0.1);
  }
  
  playCoinPickup() {
    const notes = [523.25, 659.25, 783.99]; // C5, E5, G5
    notes.forEach((freq, i) => {
      const oscillator = this.context.createOscillator();
      const gainNode = this.context.createGain();
      
      oscillator.connect(gainNode);
      gainNode.connect(this.context.destination);
      
      oscillator.type = 'sine';
      oscillator.frequency.setValueAtTime(freq, this.context.currentTime + i * 0.1);
      gainNode.gain.setValueAtTime(0.3, this.context.currentTime + i * 0.1);
      gainNode.gain.exponentialRampToValueAtTime(0.01, this.context.currentTime + i * 0.1 + 0.2);
      
      oscillator.start(this.context.currentTime + i * 0.1);
      oscillator.stop(this.context.currentTime + i * 0.1 + 0.2);
    });
  }
}

export default GameAudio;`;
    
    await fs.writeFile('assets/audio/game-audio.js', webAudioScript);
    this.state.generated.add('assets/audio/game-audio.js');
    console.log(chalk.green('  ✓ game-audio.js (Web Audio API)'));
  }
  
  async generateCombatSystem() {
    const prompt = `Generate a Bevy combat system for a JRPG with:
- Turn-based combat
- Damage calculation based on attack/defense stats
- Critical hit chance
- Status effects
Output only Rust code using Bevy ECS patterns.`;
    
    const code = await this.generate(prompt, { temperature: 0.4 });
    await fs.writeFile('src/systems/combat.rs', code);
    console.log(chalk.green('  ✓ combat system'));
  }
  
  async generateTamingSystem() {
    const prompt = `Generate a monster taming system for Bevy with:
- Taming chance based on monster health and player level
- Bait items that increase success rate
- Party management (max 6 monsters)
- Experience sharing
Output only Rust code using Bevy ECS.`;
    
    const code = await this.generate(prompt, { temperature: 0.4 });
    await fs.writeFile('src/systems/taming.rs', code);
    console.log(chalk.green('  ✓ taming system'));
  }
  
  async generateInventorySystem() {
    const prompt = `Generate an inventory system for Bevy with:
- Item storage with stack limits
- Equipment slots
- Item usage effects
- Save/load functionality
Output only Rust code using Bevy ECS.`;
    
    const code = await this.generate(prompt, { temperature: 0.4 });
    await fs.writeFile('src/systems/inventory.rs', code);
    console.log(chalk.green('  ✓ inventory system'));
  }
  
  async generateSummary() {
    const summary = {
      game: this.config.game,
      generated: {
        files: Array.from(this.state.generated),
        timestamp: new Date().toISOString(),
      },
      errors: this.state.errors,
      next_steps: [
        'Run `cargo build` to compile',
        'Run `cargo run` to test',
        'Generate actual sprite/audio assets from specifications',
        'Implement save/load system',
        'Add multiplayer support',
      ],
    };
    
    await fs.writeFile('GENERATION_SUMMARY.json', JSON.stringify(summary, null, 2));
    
    console.log(chalk.cyan('\n📊 Generation Summary:'));
    console.log(chalk.white(`  Files generated: ${this.state.generated.size}`));
    console.log(chalk.white(`  Errors: ${this.state.errors.length}`));
    console.log(chalk.white(`  Cache hits: ${this.cache.size}`));
  }
  
  // Direct execution method for AI
  async execute(command) {
    switch (command) {
      case 'generate':
        await this.generateGame();
        break;
        
      case 'regenerate':
        this.cache.clear();
        this.state.generated.clear();
        await this.generateGame();
        break;
        
      case 'test':
        console.log(chalk.cyan('🧪 Running test generation...'));
        const testResult = await this.generate('Say "Hello from AI generator"', {
          temperature: 0.1,
        });
        console.log(chalk.green(`Test result: ${testResult}`));
        break;
        
      default:
        console.log(chalk.yellow(`Unknown command: ${command}`));
    }
  }
}

// Direct execution for AI
async function main() {
  const generator = new AIGameGenerator();
  await generator.initialize();
  
  // If run with command line args
  const command = process.argv[2] || 'generate';
  await generator.execute(command);
}

// Export for use as module
module.exports = { AIGameGenerator };

// Run if called directly
if (require.main === module) {
  main().catch(error => {
    console.error(chalk.red(`\n❌ Error: ${error.message}`));
    process.exit(1);
  });
}